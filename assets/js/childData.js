// ================= RENDER CHILD TABLE WITH PAGINATION =================

function renderChildTable() {
  let filteredChildren = [...children];

  const searchValue =
    document.getElementById("childSearchInp")
      ?.value
      .toLowerCase()
      .trim() || "";

  const filterValue =
    document.getElementById("childGenderFilter")
      ?.value || "all";

  if (searchValue) {
    filteredChildren = filteredChildren.filter(child =>
      child.child_id.toLowerCase().includes(searchValue) ||
      child.child_name.toLowerCase().includes(searchValue) ||
      child.incubator_id.toLowerCase().includes(searchValue)||
       child.blood_type.toLowerCase().includes(searchValue)
    );
  }

  if (filterValue !== "all") {
    filteredChildren = filteredChildren.filter(child =>
      child.gender.toLowerCase() === filterValue.toLowerCase()
    );
  }

  childTotalPagesGlobal =
    Math.ceil(filteredChildren.length / childPerPage) || 1;

  if (childCurrentPage > childTotalPagesGlobal) {
    childCurrentPage = childTotalPagesGlobal;
  }

  const start = (childCurrentPage - 1) * childPerPage;
  const end = start + childPerPage;

  const pageChildren =
    filteredChildren.slice(start, end);

  renderChildren(pageChildren);

  document.getElementById("childPgInfo").textContent =
    `Showing ${pageChildren.length} of ${filteredChildren.length} children`;

  document.getElementById("childPages").innerHTML =
    `${childCurrentPage} / ${childTotalPagesGlobal}`;
}


